     <p><h3><strong>这里是爱心寄语</strong></h3></p>
     <p>许多在福利院长大的孩子，由于缺乏家庭关怀,心理不成熟;学习基础薄弱,自我学习能力较差，又没有一技之长，面对社会的挑战缺少</p>
     <p>基本的生存和竞争能力。大部分的大龄孤儿在初中毕顺   利升入高中，并且无法解决就业问题，、贩毒等犯罪活动。大龄孤儿助学项目</p>
     <p>旨在为这些孩子提供职业技能培训及相关的心里辅导，帮助他们自强自立，开始健康、独立的人生。</p>
     <p><strong>救助对象</strong></p>
     <p>生活在全国各福利机构中的年满16周岁、初中毕业的孤儿。</p>
