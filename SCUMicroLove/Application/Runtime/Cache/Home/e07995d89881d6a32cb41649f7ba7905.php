     <p>让大龄孤儿通过职业学校的培训，掌握诸如医护、烹饪、美容、维修、动画绘制等实用的专业技能，同时对他们进行就业指导，建立健全</p>
     <p>心理机制，树立正确健康的人生观和价值观。让他们以健康         的心态度过青春期，并在将来步入社会后能够获得较好的发展机会。</p>
